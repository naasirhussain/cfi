// booleans & comparison operator

// booleans gives only two values (true or false)

let num1 = 5;
let num2 = 15;
console.log(num1>num2);
// prints false 
console.log(num1<num2);
// prints true


// == vs ===
let num3 = "44";
let num4 = 55;
console.log(num3 == num4);
// prints false - even one is string and another is number 
// it only check value  not data type



