// Implicit Conversion

// 1. Any Datatype to String ( Using + operator)

let grade = "2" + 3;
console.log(grade, typeof grade);   // prints 23 string

grade = "4" + true;
console.log(grade, typeof grade);  // 4true string

grade = "55" + null;
console.log(grade);  //  55null

grade = "5" + undefined;
console.log(grade);   //5undefined 



