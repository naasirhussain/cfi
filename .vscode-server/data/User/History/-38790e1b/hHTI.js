//  Comparision operators

console.log(5 == '5');  // true (true converstion)
console.log(5 === '5');  // false (strict eqauality) 
// here 2 == checks only value & 3 === checks value and type

console.log(5 != '5');   // false,  it checks only value 
console.log(5 !== '5');  //  true,   it chcks value and type
console.log(0 != '0');    // false
console.log(0 !== '0');    // true
console.log(12 != '12');   // false
console.log(12 !== '12');   // true

